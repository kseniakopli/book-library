def build_series_design_prompt(
    name: str, author: str | None = None, description: str | None = None,
    lang: str = "ru",
) -> str:
    """Задача 89: паспорт оформления книжного ЦИКЛА (экслибрис + палитры).
    Опирается на описание цикла, которое читатель написал сам."""
    language = "русском" if lang == "ru" else "английском"
    about = f"\nАвтор цикла: {author}." if author else ""
    if description:
        about += f"\nО цикле (со слов читателя): {description}"
    return f"""Ты — арт-директор атмосферных литературных вечеров.
Собери «паспорт атмосферы» для книжного ЦИКЛА «{name}».{about}

Цикл — это несколько книг с общим миром: оформление должно передавать то, что
объединяет их все (сквозной мотив, эпоху, тон), а не сюжет отдельной книги.

Палитры две, обе в настроении цикла: palette_dark (тёмный фон, светлый текст)
и palette_light (светлый фон, тёмный текст) — одна атмосфера в двух освещениях,
акцентный цвет родственный. Контраст текста и фона — не ниже WCAG AA.

Нарисуй symbol_svg — минималистичный векторный символ-экслибрис цикла:
один элемент <svg viewBox="0 0 100 100"> с простыми формами (path, circle, rect,
line), цвета только из палитры, без текста, без script/обработчиков/внешних
ссылок и без атрибута href.

В поле statement — 1 предложение на {language} языке: что означает символ
и что он объединяет в цикле."""


def build_recommendations_prompt(
    favorites: list[str],
    exclude: list[str],
    count: int = 8,
    lang: str = "ru",
    disliked: list[str] | None = None,
) -> str:
    """Этап 8: советы о книгах, которых у читателя ещё нет.
    favorites — «Название — Автор (оценка)», exclude — что уже есть в библиотеке,
    disliked (з.26 ч.4) — прошлые советы, помеченные 👎."""
    language = "русском" if lang == "ru" else "английском"
    liked = "\n".join(f"- {f}" for f in favorites)
    # список исключений может быть длинным — отдаём как есть, модель сверяется с ним
    known = "\n".join(f"- {t}" for t in exclude)
    # обратная петля фидбека (з.26 ч.4): что читатель уже отклонил
    rejected = (
        "\n\nЭти советы читатель отклонил — не предлагай их и похожее:\n"
        + "\n".join(f"- {t}" for t in disliked)
        if disliked
        else ""
    )
    return f"""Ты — книжный советчик. Вот книги, которые читатель оценил высоко:
{liked}

Предложи {count} книг, которых читатель ещё НЕ читал, — по духу и темам близких
к списку выше, но не однотипных: пусть будет и очевидное попадание, и одно-два
неожиданных. Только реально существующие книги, автора указывай точно.

НЕ предлагай ничего из этого списка (уже в библиотеке):
{known}{rejected}

Для каждой книги в поле reason напиши 1–2 предложения на {language} языке:
почему именно она подойдёт ЭТОМУ читателю — со ссылкой на конкретные книги
из списка любимых, а не общими словами."""


