# ⚠ КОНТЕКСТ КНИГИ (22.07) — важное дополнение.
# Функции промптов теперь могут принимать ЧЕТВЁРТЫЙ аргумент `context` — словарь:
#   {
#     "description": "аннотация книги из Google Books (до 1200 символов)",
#     "genres": "Fiction, Contemporary",
#     "year": 2021,
#     "avoid": ["бефстроганов", "сельдь под шубой", ...],  # уже затасканное
#   }
# Зачем: без аннотации модель для малоизвестных книг УГАДЫВАЕТ по названию —
# «Капля духов в открытую рану» превратилась у Claude в арабский Дубай, хотя
# книга о московском парфюмерном мире. А `avoid` борется с однообразием:
# генерации независимы, и модель не знает, что бефстроганов она советует
# в каждой русской книге.
#
# Совместимость: если ваша функция принимает только (title, author, lang) —
# всё продолжит работать по-старому (см. `_build_with_context` в services/ai.py),
# просто контекст не попадёт в промпт. Чтобы им пользоваться — добавьте параметр.
#
# Хелпер ниже собирает контекст в текст; используйте его в своих промптах.


def format_context(context: dict | None) -> str:
    """Аннотация, жанры, год и «не повторяй это» — куском текста для промпта."""
    if not context:
        return ""
    parts = []
    if context.get("description"):
        parts.append(
            "Аннотация книги (опирайся на неё, не додумывай сюжет и географию):\n"
            f"{context['description']}"
        )
    meta = ", ".join(
        str(v) for v in (context.get("genres"), context.get("year")) if v
    )
    if meta:
        parts.append(f"Жанры и год: {meta}")
    if context.get("avoid"):
        parts.append(
            "Эти варианты уже примелькались в других книгах библиотеки — "
            "НЕ предлагай их, найди другие:\n- " + "\n- ".join(context["avoid"])
        )
    return "\n\n".join(parts)


