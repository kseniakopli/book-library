// Картинка может не загрузиться: битая ссылка на обложку, обрезанный SVG-символ.
// Хук убирает повторяющуюся пару «состояние + onError» (ревью 19.07, задача 76):
// раньше она была скопирована четырежды — по два раза в BookCard и BookDetail.
//
// Компонентом это делать не стали: у обложки и символа разные обёртки и цепочка
// фолбэков (обложка → символ → «Нет обложки»), поэтому структуру оставляем
// вызывающему, а хук снимает только рутину.
import { useState } from "react";

export function useImageFallback() {
  const [broken, setBroken] = useState(false);
  return {
    broken,
    onError: () => setBroken(true),
    ok: (src) => Boolean(src) && !broken,   // «есть ссылка и она рабочая»
  };
}
